import { Button } from "@mantine/core";

export type Initial = {
  username: string;
  mailAddres: string;
  password: string;
};

function Login() {
  const initialValues = { username: "", mailAddres: "", password: "" };
  const [formValues, setFormValues] = useState(initialValues);
  const [formErrors, setFormErrors] = useState(initialValues);
  const [isSubmit, setIsSubmit] = useState(false);
  const router = useRouter();

  function handleChange(props: ChangeEvent<HTMLInputElement>) {
    const { name, value } = props.target;
    setFormValues({ ...formValues, [name]: value });
  }

  function handleSubmit(props: FormEvent<HTMLFormElement>) {
    props.preventDefault();
    const { isError, ...error } = Validate(formValues);

    // errorがあったら
    if (isError) {
      setFormErrors(error);
    } else {
      router.push({ pathname: "/todo" });
    }

    //setFormErrors(error);
    //setIsSubmit(true);
  }

  function Demo() {
    return (
      <Button color="lime" radius="md" size="md" onSubmit={() => handleSubmit}>
        Settings
      </Button>
    );
  }

  return (
    <>
      <form onSubmit={(e) => handleSubmit(e)}>
        <h1>ログインフォーム</h1>
        <hr />
        <div>
          <div>
            <label>ユーザー名</label>
            <input
              type="text"
              placeholder="ユーザー名"
              name="username"
              onChange={(e) => handleChange(e)}
            ></input>
          </div>
          <p className="errorMsg">{formErrors.username}</p>
          <div className="formField">
            <label>メールアドレス</label>
            <input
              type="text"
              placeholder="メールアドレス"
              name="mailAddres"
              onChange={(e) => handleChange(e)}
            ></input>
          </div>
          <p className="errorMsg">{formErrors.mailAddres}</p>
          <div className="formField">
            <label>パスワード</label>
            <input
              type="text"
              placeholder="パスワード"
              name="password"
              onChange={(e) => handleChange(e)}
            ></input>
          </div>
          <p className="errorMsg">{formErrors.password}</p>
        </div>
        <Demo />
      </form>

      {
        //<Link href="/todo"><button>ログインテスト</button></Link>
      }
    </>
  );
}

export default Login;
